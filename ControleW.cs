//Estruturas de controle - While: A estrutura ‘While’ é uma estrutura de repetição, ou seja, determinada ação desejada
// pelo programador será repetida enquanto a condição é satisfeita.

class ControleW{
    static void Main(String [] args ){
       var n1 = 2;

       while(n1 < 8){
          Console.WriteLine(n1);
          n1++;
       }
    }
}